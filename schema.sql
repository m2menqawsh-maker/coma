    SELECT * FROM product_recipes
    WHERE product_id = NEW.product_id
      AND (size IS NULL OR size = NEW.size)
  LOOP
    UPDATE inventory_items
    SET qty = qty - (rec.qty_per_unit * NEW.quantity)
    WHERE id = rec.inventory_item_id;

