CREATE VIEW public.cash_balance WITH (security_invoker='true') AS
 SELECT (COALESCE(sum(
        CASE
            WHEN (direction = 'in'::public.ledger_direction) THEN amount
            ELSE (0)::numeric
        END), (0)::numeric) - COALESCE(sum(
        CASE
            WHEN (direction = 'out'::public.ledger_direction) THEN amount
            ELSE (0)::numeric
        END), (0)::numeric)) AS balance
   FROM public.ledger_entries
  WHERE ((channel = 'cash'::public.payment_channel) AND (tx_type <> 'debt_create'::public.ledger_tx_type));


