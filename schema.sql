--
-- Name: bank_account_balances; Type: VIEW; Schema: public; Owner: postgres
--

CREATE VIEW public.bank_account_balances WITH (security_invoker='true') AS
 SELECT id,
    name,
    account_type,
    phone,
    is_active,
    COALESCE(( SELECT (sum(
                CASE
                    WHEN ((le.direction = 'in'::public.ledger_direction) AND ((le.transfer_status IS NULL) OR (le.transfer_status = 'confirmed'::public.transfer_status))) THEN le.amount
                    ELSE (0)::numeric
                END) - sum(
                CASE
                    WHEN (le.direction = 'out'::public.ledger_direction) THEN le.amount
                    ELSE (0)::numeric
                END))
           FROM public.ledger_entries le
          WHERE ((le.bank_account_id = ba.id) AND (le.channel = 'bank'::public.payment_channel))), (0)::numeric) AS balance
   FROM public.bank_accounts ba;


ALTER VIEW public.bank_account_balances OWNER TO postgres;

--
-- Name: bank_balance_by_account; Type: VIEW; Schema: public; Owner: postgres
--

CREATE VIEW public.bank_balance_by_account WITH (security_invoker='true') AS
 SELECT ba.id,
    ba.name,
    ba.account_type,
    (COALESCE(sum(
        CASE
            WHEN ((le.direction = 'in'::public.ledger_direction) AND ((le.transfer_status IS NULL) OR (le.transfer_status = 'confirmed'::public.transfer_status))) THEN le.amount
            ELSE (0)::numeric
        END), (0)::numeric) - COALESCE(sum(
        CASE
            WHEN (le.direction = 'out'::public.ledger_direction) THEN le.amount
            ELSE (0)::numeric
        END), (0)::numeric)) AS balance
   FROM (public.bank_accounts ba
     LEFT JOIN public.ledger_entries le ON (((le.bank_account_id = ba.id) AND (le.channel = 'bank'::public.payment_channel))))
  WHERE (ba.is_active = true)
  GROUP BY ba.id, ba.name, ba.account_type;


