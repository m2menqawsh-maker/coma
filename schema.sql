-- Name: customer_balances; Type: VIEW; Schema: public; Owner: postgres
--

CREATE VIEW public.customer_balances WITH (security_invoker='true') AS
 SELECT c.id,
    c.name,
    c.phone,
    c.is_vip,
    COALESCE(sum(i.debt_created), (0)::numeric) AS total_debt_created,
    COALESCE(sum(cdp.amount), (0)::numeric) AS total_debt_paid,
    (COALESCE(sum(i.debt_created), (0)::numeric) - COALESCE(sum(cdp.amount), (0)::numeric)) AS remaining_debt,
    COALESCE(sum(i.credit_applied), (0)::numeric) AS total_credit_used,
    c.last_visit_at
   FROM ((public.customers c
     LEFT JOIN public.invoices i ON ((i.customer_id = c.id)))
     LEFT JOIN public.customer_debt_payments cdp ON ((cdp.customer_id = c.id)))
  GROUP BY c.id, c.name, c.phone, c.is_vip, c.last_visit_at;


